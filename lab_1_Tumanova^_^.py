# ---------- СОЗДАНИЕ И ЗАПОЛНЕНИЕ БАЗЫ ----------
conn = sqlite3.connect(DB_NAME)
conn.execute('PRAGMA foreign_keys = ON')
cursor = conn.cursor()

cursor.execute('DROP TABLE IF EXISTS Orders')
cursor.execute('DROP TABLE IF EXISTS Products')
cursor.execute('DROP TABLE IF EXISTS Users')

cursor.execute('''
CREATE TABLE Users (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    first_name TEXT NOT NULL,
    last_name TEXT NOT NULL,
    age INTEGER,
    gender TEXT CHECK(gender IN ('Male', 'Female', 'Other')),
    profession TEXT,
    salary REAL,
    city TEXT,
    registration_date DATE DEFAULT CURRENT_DATE
)
''')

cursor.execute('''
CREATE TABLE Products (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    price REAL NOT NULL,
    quantity INTEGER DEFAULT 0,
    expiration_date DATE
)
''')

cursor.execute('''
CREATE TABLE Orders (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    order_number TEXT NOT NULL,
    user_id INTEGER NOT NULL,
    product_id INTEGER NOT NULL,
    order_date DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES Users(id) ON DELETE CASCADE,
    FOREIGN KEY (product_id) REFERENCES Products(id) ON DELETE CASCADE
)
''')

cursor.executemany('''
INSERT INTO Users (first_name, last_name, age, gender, profession, salary, city, registration_date)
VALUES (?, ?, ?, ?, ?, ?, ?, ?)
''', users_mas)

cursor.executemany('''
INSERT INTO Products (name, price, quantity, expiration_date)
VALUES (?, ?, ?, ?)
''', products_mas)

cursor.executemany('''
INSERT INTO Orders (order_number, user_id, product_id, order_date)
VALUES (?, ?, ?, ?)
''', orders_mas)

conn.commit()

# ---------- ФУНКЦИЯ ДЛЯ КРАСИВОГО ВЫВОДА ----------
def print_query(title, query):
    print('\n' + '=' * 90)
    print(title)
    print('=' * 90)
    cursor.execute(query)
    for row in cursor.fetchall():
        print(row)

# Проверка количества строк
print_query('Количество строк в таблицах', '''
SELECT 'Users' AS table_name, COUNT(*) FROM Users
UNION ALL SELECT 'Products', COUNT(*) FROM Products
UNION ALL SELECT 'Orders', COUNT(*) FROM Orders
''')

# 1. Вывести список продуктов от дорогих к дешевым
print_query('1. Продукты от дорогих к дешевым', '''
SELECT id, name, price, quantity, expiration_date
FROM Products
ORDER BY price DESC
''')

# 2. Вывести пользователей из г. Москва
print_query('2. Пользователи из Москвы', '''
SELECT id, first_name, last_name, age, gender, profession, salary, city, registration_date
FROM Users
WHERE city = 'Москва'
ORDER BY last_name
''')

# 3. Вывести пользователей, чья зарплата выше средней
print_query('3. Пользователи, чья зарплата выше средней', '''
SELECT id, first_name, last_name, profession, salary
FROM Users
WHERE salary > (SELECT AVG(salary) FROM Users)
ORDER BY salary DESC
''')

# 4. Вывести список заказов с указанием имени пользователя и названия продукта
print_query('4. Заказы с именем пользователя и названием продукта', '''
SELECT
    o.id,
    o.order_number,
    u.first_name || ' ' || u.last_name AS user_name,
    p.name AS product_name,
    p.price,
    o.order_date
FROM Orders o
INNER JOIN Users u ON o.user_id = u.id
INNER JOIN Products p ON o.product_id = p.id
ORDER BY o.order_date
''')

# 5. Вывести пользователей, группируя их по профессии
print_query('5. Пользователи, сгруппированные по профессии', '''
SELECT
    profession,
    COUNT(*) AS user_count,
    ROUND(AVG(age), 1) AS avg_age,
    ROUND(AVG(salary), 2) AS avg_salary
FROM Users
GROUP BY profession
ORDER BY user_count DESC, profession
''')

# 6. Вывести заказы, совершенные позже 2026-03-10
print_query('6. Заказы, совершенные позже 2026-03-10', '''
SELECT
    o.id,
    o.order_number,
    u.first_name || ' ' || u.last_name AS user_name,
    p.name AS product_name,
    o.order_date
FROM Orders o
INNER JOIN Users u ON o.user_id = u.id
INNER JOIN Products p ON o.product_id = p.id
WHERE DATE(o.order_date) > DATE('2026-03-10')
ORDER BY o.order_date
''')

